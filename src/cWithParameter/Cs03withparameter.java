package cWithParameter;

public class Cs03withparameter {
	
	public static void addi03(int a,int b ) //two parameter
	{
		int sum =a+b;
			System.out.println("addition is "+sum);
	}

	
	public void addi03 (int a,int b,int c ) //three parameter
	{
		int sum =a+b+c;
			System.out.println("addition is "+sum);
	}
	
}
